    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        //kiểm tra nhập ngày đúng định dạng
        try{
            //tạo đối tượng SimpleDateFormat
            SimpleDateFormat formater = new SimpleDateFormat("dd-MM-yyyy");
            //lấy gtri trên o text Ngày sinh
            String text = this.txtNgaySinh.getText();
            Date date = formater.parse(text);
            JOptionPane.showMessageDialog(this, date);
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Không đúng định dang");
            this.txtNgaySinh.requestFocus();
            return;
        }finally{
            JOptionPane.showMessageDialog(this, "Hôm nay trời đẹp quá !!!");
        }
    }//GEN-LAST:event_btnOkActionPerformed

